# Variables
# Nos permite almacenar datos, cada uno tendra un nombre y un valor (nombre = valor)

## Tipos de Datos
# Texto -> str
nombre = "Jeancarlos"
apellido_paterno = "De la cruz"

# Numeros Enteros (Positivos y Negativos) -> int
edad = 28
mes_numero = 12

# Numeros Flotantes -> float
pi = 3.1415
precio_pan = 0.30

# Boleanos (Verdadero | Falso) -> bool
# En javascript -> true - false
# En python -> True - False
soltero = False
vivo = True
