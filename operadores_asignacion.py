## Operadores de Asignaci√≥n
# Operador igual
x = 10
print(f'Valor inicial: {x}')

# Incremento
x += 2
print(f'Valor incrementado: {x}')

# Reducir
x -= 4
print(f'Valor reducido: {x}')

# Multiplicador
x *= 2
print(f'Valor multiplicado: {x}')

# Dividendo
x //= 4
print(f'Valor dividendo: {x}')

# Potencia
x **= 3
print(f'Valor potenciado: {x}')
